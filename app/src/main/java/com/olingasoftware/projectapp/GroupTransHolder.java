package com.olingasoftware.projectapp;

public class GroupTransHolder {
    private String userAdmin;
    private String amountDeposit;
    private String sourceIncome;
    private String amountWithdraw;
    private String time;

    public GroupTransHolder() {
    }

    public String getUserAdmin() {
        return userAdmin;
    }

    public void setUserAdmin(String userAdmin) {
        this.userAdmin = userAdmin;
    }

    public String getAmountDeposit() {
        return amountDeposit;
    }

    public void setAmountDeposit(String amountDeposit) {
        this.amountDeposit = amountDeposit;
    }

    public String getSourceIncome() {
        return sourceIncome;
    }

    public void setSourceIncome(String sourceIncome) {
        this.sourceIncome = sourceIncome;
    }

    public String getAmountWithdraw() {
        return amountWithdraw;
    }

    public void setAmountWithdraw(String amountWithdraw) {
        this.amountWithdraw = amountWithdraw;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
